/* Decrement Operator
In JavaScript we use the decrement operator to decrease a value stored in a variable. The decrement could be pre or post decrement. Let us see each of them: */

//Pre-decrement
let count = 0
console.log(--count) // -1
console.log(count)  // -1

//Post-decrement
let $count = 0
console.log($count--) // 0
console.log($count)   // -1