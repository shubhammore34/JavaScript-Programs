// Declaring different variables of different data types

let firstName = 'Shubham'; // first name of a person
let lastName = 'More';   // last name of a person
let country = 'India';    // country
let city = 'Mumbai';      // capital city
let age = 20;              // age in years
let isMarried = false;
console.log(firstName, lastName, country, city, age, isMarried); // Shubham, More, India, Mumbai, 20, False

// Declaring variables with number values

const gravity = 9.81;                    // earth gravity  in m/s2
const boilingPoint = 100;                // water boiling point, temperature in oC
const PI = 3.14;                         // geometrical constant
console.log(gravity, boilingPoint, PI);  // 9.81, 100, 3.14

// Variables can also be declaring in one line separated by comma

let name = 'Shubham', //name of a person
    hobby = 'Chess'
    live = 'India';
console.log(name, hobby, live);